    private void TblDataPinjamanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TblDataPinjamanMouseClicked
        index = TblDataPinjaman.getSelectedRow();
        
        nourut = (String) TblDataPinjaman.getValueAt(index, 1);
        
        noanggota = (String) TblDataPinjaman.getValueAt(index, 2);
        namaanggota = (String) TblDataPinjaman.getValueAt(index, 3);
        judul_buku = (String) TblDataPinjaman.getValueAt(index, 4);
        tanggal_pinjam = (String) TblDataPinjaman.getValueAt(index, 5);
        lama_pinjam = (String) TblDataPinjaman.getValueAt(index, 6);
        status = (String) TblDataPinjaman.getValueAt(index, 7);
         
        TxtNoUrut.setText(nourut);
        TxtNoAnggota.setText(noanggota);
        TxtNamaAnggota.setText(namaanggota);
        TxtJudulBuku.setText(judul_buku);
        CmbStatus.setSelectedIndex(index);
        TxtHari.setText(lama_pinjam);
        
        try {
            SimpleDateFormat tgl1 = new SimpleDateFormat("yyyy-MM-dd");
            Date dt = format_tanggal.parse(tanggal_pinjam);
            tgl1.format(dt);
            TglPinjam.setDate(dt);
        } catch(Exception ex){
            System.out.println(ex);
        }
        
    }//GEN-LAST:event_TblDataPinjamanMouseClicked

