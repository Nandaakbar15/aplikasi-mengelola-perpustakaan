     public void LoadTable(){
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("id");
        model.addColumn("NO_URUT");
        model.addColumn("NO_ANGGOTA");
        model.addColumn("NAMA_ANGGOTA");
        model.addColumn("JUDUL_BUKU");
        model.addColumn("TANGGAL_PINJAM");
        model.addColumn("LAMA_PINJAM");
        model.addColumn("STATUS");
        
         try {
//            int no = 1;
            String sql = "SELECT * FROM daftar_pinjaman";
            java.sql.Connection conn = (Connection)Koneksi.getConnection();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            while(res.next()){
                model.addRow(new Object[]{res.getString("id"), 
                res.getString("NO_URUT"), res.getString("NO_ANGGOTA"), 
                res.getString("NAMA_ANGGOTA"), res.getString("JUDUL_BUKU"),res.getString(6), 
                res.getString(7), res.getString("STATUS")});
            }
            TblDataPinjaman.setModel(model);
        } catch (Exception e){
            
        }
    }
     
